def _ordered_dedup(symbols):
    """Deduplicate while preserving insertion order (first occurrence wins)."""
    seen = set()
    result = []
    for s in symbols:
        su = s.upper().strip()
        if su not in seen:
            seen.add(su)
            result.append(su)
    return result


