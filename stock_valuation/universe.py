# Compose named universes, deduplicating across all lists.
# ============================================================================


def _dedup(symbols: list[str]) -> list[str]:
    """Return a sorted, deduplicated list of symbols."""
    seen: set[str] = set()
    result: list[str] = []
    for s in symbols:
        s = s.strip()
        if s and s not in seen:
            seen.add(s)
            result.append(s)
    return sorted(result)


